	<h2>Procedures</h2>		
	<ul>
		<li>After submission of the application by the user/applicant.</li>
		<li>First Facilitator will receive and check the application and then forward it to Field Assistant</li>
		<li>The Field Assistant will receive and verify the application.</li>
		<li>After verification, the Field Assistant will submit the report of the application to the Deputy Licensing Officer.</li>
		<li>The Deputy Licensing Officer will forward the report to the Deputy Commissioner.</li>
		<li>The Deputy Commissioner (D.C.) approves the report.	</li>
		<li>The user/applicant pays the fees and after payment, The Licence will be issued.</li>
	</ul>
</div>
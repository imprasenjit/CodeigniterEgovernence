	<h2>Procedures</h2>		
	<ol>
		<li>After paying the requisite challan of Rs-50 along with the all relevant documents the party who forms a firm should apply to the RFS office</li>
		<li>Dealing Asst. of relevant jurisdiction will scrutinize the entire documents thoroughly and if any clarification required he/ she can ask the applicant with the consent of RFS.</li>
		<li>On satisfactory compliance of all required documents. Concerned Dealing Asst. will forward the file to RFS.</li>
		<li>After scrutinizing the file and found the file satisfactorily complied then RFS will registrar the firm and issue relevant certificate</li>		
	</ol>
</div>
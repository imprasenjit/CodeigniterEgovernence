	<h2>Procedures</h2>		
	<h4><strong>Pollution procedure (Consent to Establish/Consent to operate/Hazardous waste)</strong></h4>
	<ul>
		<li>The applicant fills up the form online and submits it.</li>
		<li>The Facilitator(Regional office) receives the form. He can either reject the form, process it or ask the applicant for additional information.</li>
		<li>If the Facilitator rejects the form, the applicant  will get a rejection mail with comments</li>
		<li>If the Facilitator processes the form, then the form is forwarded to  the Inspector</li>
		<li>If the Facilitator asks for additional information,  the applicant  provides the Facilitator with the required information</li>
		<li>Then the Inspector sends a mail to the applicant  mentioning the fees to be paid</li>
		<li>The Inspector will intimate the applicant  about the inspection date via email</li>
		<li>The Inspector goes to the site for a physical inspection</li>
		<li>He submits an inspection report  after which the report is forwarded to the Engineer(Head Office)</li>
		<li>The Engineer can either reject it or ask the applicant for additional information</li>
		<li>If he rejects it then the applicant gets a mail with rejection comments</li>
		<li>If he asks the applicant for additional information, ,  the applicant  provides the Engineer with the required information</li>
		<li>If everything complies, the Chairman issues the certificate</li>
	</ul>
</div>
	<h4><strong>Renewal of Consent to Operate</strong></h4>
	<p>Application for Consent to Operate (Renewal) is to be submitted in the 4th month before the expiry of the Consent order. If application is made after expiry of the validity period an action is to be taken as per Section 25/26 of Water Act & Section 21 of Air Act.</p>
	<p>The application form for Consent to Operate (Renewal) under the Water Act and Air Act is Schedule-I should be submitted in triplicate. The application form fees Rs. 100/- and is available at all the offices of the Board or can be <a href="http://pcbassam.org/pdf/schedule-i.pdf" target="blank">downloaded</a> from the Website.</p>

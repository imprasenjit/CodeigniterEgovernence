	<h2>Inspection Procedure</h2>		
	<ol>
		<li>Facilitator forwards the filled up form of the applicant to the Inspector for review</li>
		<li>The Inspector reviews the form, clicks on the Inspection tab on the system and selects a particular inspection date after which an email is sent to the applicant wherein the inspection date will be mentioned. It will also be mentioned that on the day of inspection the applicant will have to furnish physical copies of  all the documents mentioned on the checklist that he/she had uploaded earlier </li>
		<li>The Inspector visits the site on the pre selected date for inspection</li>
		<li>After he is done with the inspection he needs to submit the inspection report and upload the same on the system within 48 hours i.e. two working days(excluding weekends) from the date of inspection</li>
	</ol>
	<h2>Inspection Guidelines</h2>		
	<ol>
		<li>The Inspector should visit the site on pre selected date</li>
		<li>He should submit the inspection report in a particular format which is yet to be designed</li>
		<li>He should submit the inspection report  within 48 hours i.e two working days from the date of inspection</li>
		<li>The Inspector should click on the inspection tab on the system and select a particular date</li>
	</ol>
	

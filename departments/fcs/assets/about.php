	<h2>About Us</h2>								
	

<p>
All activities of the office of the Chief Inspector of Boilers runs according to Boilers Act,
Rules and Regulations. Chief Inspector of Boilers is the head of the office. Deputy chief
Inspector of Boilers and Inspector of  Boilers  are  direct   sub-ordinates  to  the  Chief
Inspector of Boilers. Inspectors of Boilers are the field level officers assigned for specific
areas. Inspector of Boilers submits report to Chief Inspector of Boilers after proper inspe-
ction of boilers and Chief Inspector of Boilers takes necessary step to issue different
certificate of boilers.</p>

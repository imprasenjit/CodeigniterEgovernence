	<h2>Time Limits</h2>		
	<p>The Chief Inspector of Factories, being satisfied that the plans are in consonance with the requirement of the Act shall subject to such conditions as he may specify, approve them within the period specified in the table given below:</p>
	<table class="table table-responsive">
	    <tr>
	        <th>Sr No.</th>
	        <th>Category of Factories</th>
	        <th>Time limit from the date of receipt of completed application</th>
	    </tr>
	    <tr>
	        <td>1.</td>
	        <td>Factories as defined under section2(m) (ii) and involving from non-hazardous process as defined under rule 2(m)</td>
	        <td>10 Days</td>
	    </tr>
	    <tr>
	        <td>2.</td>
	        <td>Factories other than hazardous and MAH units</td>
	        <td>30 days</td>
	    </tr>
	    <tr>
	        <td>3.</td>
	        <td>MAH/Hazardous Factories(as defined under section2(cb) of the factories Act 1948 and MSIHC Rules,1989.</td>
	        <td>45 days from the date of the site of the factory has been appraised by the Site appraisal committee and approved by the Govt.</td>
	    </tr>
	</table>

	<h2>About Us</h2>								
	<p>Guwahati Municipal Corporation (GMC) is the urban local body responsible for governing, developing and managing Guwahati. It administers an area of 216.79 km<sup>2</sup> of Guwahati city. The entire GMC area is divided into 31 municipal wards and each municipal ward is further divided into 2, 3 or 4 Area Sabhas. Presently, there are 90 Area Sabhas. GMC works under Guwahati Development Department, Assam.
	</p>
	<p>The GMC is the creation of the Guwahati Municipal Corporation Act 1971. The corporation was duly constituted in 1974 in the first meeting of the elected councillors as per provision of Sec.45 of this Act.
	</p>
	<p>GMC administers through various branches/sections/cells located at its different offices situated mainly at various places of Guwahati city. The municipal services provided by GMC are- 
	</p>
	<ul>
		<li>Property assessment/reassessment/mutation </li>
		<li>Development and maintenance of roads/drains</li>
		<li>Issue and renewal of trade license.</li>
		<li>Development and maintenance of municipal markets.</li>
		<li>Construction and maintenance of Public Toilets/e-Toilets </li>
		<li>Building permission.</li>
		<li>Development and allotment of parking slots.</li>
		<li>Development and maintenance of parks and ponds.</li>
		<li>Hoarding and advertisement.</li>
		<li>Issue of Birth/Death certificates.</li>
		<li>Installation and maintenance of Street Lights</li>
		<li>Cesspool emptier</li>
		<li>Construction and maintenance of crematorium.</li>
		<li>Implementation of different central/state Government programmes schemes and projects.</li>
		<li>Water supply with metered/non-metered connection. etc.</li>
	</ul>
	<p>
	For property tax and trade licence purposes, the corporation area is divided into six revenue zones.
	</p>
	<p>For Conservancy and Public works, the corporation area is divided into six engineering divisions.
	</p>
	<p>There are over 1.87 Lakh (approx) assessed holdings and about 30,000 water connections to different commercial and residential households.
	</p>

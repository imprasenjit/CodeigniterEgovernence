	<h2>Procedures</h2>	
	<table  class="table table-respnsive">
		<tr>
			<td width="8%">step 1</td>
			<td>The applicant submits form , If he submits the fee offline, then he will have to upload a copy of the challan</td>
		</tr>
		<tr>
			<td>step 2</td>
			<td>The Facilitator(as per district) receives the form. He can either reject the form, process it or ask the user for additional information.</td>
		</tr>
		<tr>
			<td>step 3</td>
			<td>If the Facilitator rejects the form, the applicant  will get a rejection mail with comments</td>
		</tr>
		<tr>
			<td>step 4</td>
			<td>If the Facilitator processes the form, then the form is forwarded to the Inspector</td>
		</tr>
		<tr>
			<td>step 5</td>
			<td>If the Facilitator asks for additional information,  the applicant  provides the Facilitator with the required information</td>
		</tr>
		<tr>
			<td>step 6</td>
			<td>The Inspector  intimates the user about the inspection date via email</td>
		</tr>
		<tr>
			<td>step 7</td>
			<td>The  Inspector goes to the site for physical inspection on the predetermined date</td>
		</tr>
		<tr>
			<td>step 8</td>
			<td>He submits an inspection report  after which the report is forwarded to the Chief Inspector of Factories</td>
		</tr>
		<tr>
			<td>step 9</td>
			<td>The Chief Inspector can either reject or approve it.</td>
		</tr>
		<tr>
			<td>step 10</td>
			<td>If he rejects it then the applicant gets a mail with rejection comments</td>
		</tr>
		<tr>
			<td>step 11</td>
			<td>If he approves it, then he issues the certificate</td>
		</tr>
		

	</table>	

	

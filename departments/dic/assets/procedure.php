	<h2>Procedures</h2>		
	<h4><strong>Entrepreneur Memorandum Part I</strong></h4>
	<p><strong>Entrepreneur Memorandum Part I is for the proposed enterprises.</strong></p>
	<ul>
		<li>Any person who intends to establish a micro, small or medium enterprise engaged in providing or rendering of services may file or those who want to establish medium enterprise engaged in the production or manufacture of products shall file Part-I of the Entrepreneurs Memorandum to District Industries and Commerce Centre (DICC).</li>
		<li>Once the above enterprises start production or start providing or rendering services, they shall file Part II of the Enterpreneurs Memorandum to District Industries and Commerce Centre.</li>
		<li>In case of change in the investment in plant and machinery or in equipment, the enterprises who have already filed Entrepreneurs Memorandum should inform the Discrict Industries Centre of the same in writing within three month of the change in investment.</li>
		<li>In case of change of products and that of services or addition in products or services, the enterprises which have already filed Entrepreneurs Memorandum shall inform the District Industries Centre of the same in writing within three months of the change.</li>
		<li>In respect of those who furnish wrong information and do manipulations in the online application form penal action will be taken by the Department and simultaneously online Entrepreneur Memorandum will be automatically cancelled and displayed in the website.</li>
		<li>The Entrepreneur Memorandum is subject to any or all conditions that maybe imposed by the Acknowledging Authority from time to time.</li>
	</ul>
	<p><strong>ENTREPRENURS MEMORENDUM PART –II (EM-part-II)</strong></p>
	<p>Enterpreneur Memorandum Part II is to the established enterprises after commencement of commercial production.</p>
	<ul>
		<li>After filing Online, please ensure to submit all the documents/papers as the case may to the concerned general Manager, District Industries & Commerce Centre within 3 (three) days time</li>
		<li>Before filing please ensure, the list of machinery and list of employee are ready as per Annexure-A & B in the Checklist.</li>
		<li>An Illustrative list of NIC & ASICC codes are available with this site, check the relevant Code and unit and in any difficulty or doubt, contact the DICC official as these are compulsory filed.</li>
		<li>Check list of all the documents etc</li>
	</ul>
	<p><strong>Check List</strong></p>
	<p><strong>Constitution of the unit</strong></p>
	<ol>
		<li>In case of Private Limited / Public Limited company
		<ul>
			<li>Registration Certificate under Companies Act</li>
			<li>Memorandum and Article of Association</li>
			<li>Name & Address of the Partners with their PAN no if any</li>
		</ul></li>
		<li>In case of Partnership unit 
		<ul>
			<li>Deed of partnership</li>
			<li>General Power of Attorney</li>
			<li>Name & Address of the Partners with their PAN no if any</li>
		</ul></li>
		<li>In case of Co-operative Society
		<ul>
			<li>Registration Certificate from the Jt. Register of Co-operative Society</li>
			<li>Article of Memorandum of Association</li>
			<li>Resolution of the General Body for registration of the unit.</li>
		</ul></li>
	</ol>
	<p><strong>Registration</strong></p>
	<ul>
		<li>A copy of Entrepreneurs Memorandum Part I /Provisional Registration Certificate, a copy IEM issued by SIA in case of conversion to EM part-II (as applicable)</li>
	</ul>
	<p><strong>Land & Building</strong></p>
	<ol>
		<li>In case of own land
		<ul>
			<li>Purchase deed / gift deed/any other document to establish the ownership wherever applicable</li>
			<li>Upto date non-incumbent certificate wherever applicable</li>
			<li>Jamabandi copy and trace map wherever applicable</li>
		</ul></li>
		<li>In case of Industrial land/plot allotted by any Government Agency 
		<ul>
			<li>Deed of agreement</li>
			<li>Up to date rent payment receipt</li>
		</ul></li>
		<li>In case of Industrial shed/s allotted by any Government Agency
		<ul>
			<li>Deed of agreement</li>
			<li>Up to date rent payment receipt wherever applicable</li>
		</ul></li>
		<li>In case of leasehold land from a private owner
		<ul>
			<li>Lease deed agreement</li>
		</ul></li>
		<li>In case of Government land/plot allotted by Government
		<ul>
			<li>Allotment letter</li>
			<li>Premium payment receipt whenever applicable</li>
		</ul></li>
		<li>Statement of Plant and Machinery</li>
	</ol>
